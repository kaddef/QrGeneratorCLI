package core

import "strconv"

// We cant spot the L 01 Or 0 mask 000 because we are applaying mask
var FormatLookupTable = map[string]int{
	"L0": 0b111011111000100,
	"L1": 0b111001011110011,
	"L2": 0b111110110101010,
	"L3": 0b111100010011101,
	"L4": 0b110011000101111,
	"L5": 0b110001100011000,
	"L6": 0b110110001000001,
	"L7": 0b110100101110110,
	"M0": 0b101010000010010,
	"M1": 0b101000100100101,
	"M2": 0b101111001111100,
	"M3": 0b101101101001011,
	"M4": 0b100010111111001,
	"M5": 0b100000011001110,
	"M6": 0b100111110010111,
	"M7": 0b100101010100000,
	"Q0": 0b011010101011111,
	"Q1": 0b011000001101000,
	"Q2": 0b011111100110001,
	"Q3": 0b011101000000110,
	"Q4": 0b010010010110100,
	"Q5": 0b010000110000011,
	"Q6": 0b010111011011010,
	"Q7": 0b010101111101101,
	"H0": 0b001011010001001,
	"H1": 0b001001110111110,
	"H2": 0b001110011100111,
	"H3": 0b001100111010000,
	"H4": 0b000011101100010,
	"H5": 0b000001001010101,
	"H6": 0b000110100001100,
	"H7": 0b000100000111011,
}

func GetFormatValue(ecLevel string, mask int) (int, bool) {
	key := ecLevel + strconv.Itoa(mask)
	value, exists := FormatLookupTable[key]
	return value, exists
}
